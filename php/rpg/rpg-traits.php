            <div class="line-container">
              <h5><strong>Conditions</strong></h5>
              <div class="line"></div>
            </div>
            <p class="copy">Condition Traits are Traits that represent some sort of ongoing status. These will typically describe some sort of ongoing effect a character is experiencing, whether physical or mental. Condition Traits can vary widely in what they represent and will almost always have their effects described in the Trait. These can range from a character having cybernetic arms, granting them significantly improved power stats to a character being a conduit for the spirits and thus able to see ghosts. Injuries will often be represented as temporary Condition Traits and will describe some sort of negative attribute or incapacity forced onto the character.

            <div class="line-container">
              <h5><strong>Other</strong></h5>
              <div class="line"></div>
            </div>
            <p class="copy">Other is a catchall category and represents special Traits that go beyond the norm. Most Other Traits are, in reality, multiple Traits listed at the same time for the sake of convenience. As an example, a character being possessed by a daemon may have that possession listed as an Other Trait, and that Traits description may describe significant alterations to how that character would otherwise function.</p>
          </div>

